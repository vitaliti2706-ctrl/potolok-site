// api/telegram-submit.js

export default async function handler(req, res) {
  if (req.method !== "POST") {
    return res.status(405).json({ error: "Method not allowed" });
  }

  const { name, phone, message } = req.body;

  try {
    const telegramRes = await fetch(
      `https://api.telegram.org/bot{process.env.TELEGRAM_BOT_TOKEN}/sendMessage`,
      {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({
          chat_id: process.env.TELEGRAM_CHAT_ID,
          text: `–ó–∞—è–≤–∫–∞ —Å —Å–∞–π—Ç–∞\nüë§ –ò–º—è: ${name}\nüìû –¢–µ–ª–µ—Ñ–æ–Ω: ${phone}\nüí¨ –°–æ–æ–±—â–µ–Ω–∏–µ: ${message}`
        }),
      }
    );

    const data = await telegramRes.json();

    if (!data.ok) {
      return res.status(500).json({ error: "–û—à–∏–±–∫–∞ Telegram API", details: data });
    }

    return res.status(200).json({ success: true, data });
  } catch (err) {
    return res.status(500).json({ error: "–û—à–∏–±–∫–∞ —Å–µ—Ä–≤–µ—Ä–∞", details: err.message });
  }
}







